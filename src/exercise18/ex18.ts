const locations_arr = ["Makkah", "Turkey", "Madina", "Pakistan", "Himalya"];

console.log(locations_arr, "--> Original Array <--");

console.log(locations_arr.toSorted(), " --> Sorted Array <--");

console.log(locations_arr, "--> Original array is Intact<--");

console.log(locations_arr.toReversed(), "--> Reversed Array <--");

console.log(locations_arr, "--> Array Intact <--");

console.log(locations_arr.reverse(), "--> Original Array is reversed <--");

console.log(
  locations_arr.reverse(),
  "--> Original Array is reversed again <--"
);

console.log(locations_arr.sort(), "--> Original Array Sorted <--");

console.log(locations_arr.reverse(), "--> original array is reversed<--");
